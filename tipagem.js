//linguagens interpretadas:
/* Na linguagem interpretada, nós vamos escrever o nosso código e o nosso código vai ser executado. Então, nós vamos ter um programa por trás, que vai ler
a linha do nosso código e executar o que está escrito nessa linha na hora em que estivermos rodando.
Já linguagens compiladas, nosso código vai passar primeiro por um programa chamado compilador, que vai transformar o nosso código em código de máquina, 
linguagem de máquina. É um tipo especial de linguagem que o computador entende, que roda diretamenta na nossa máquina e também rodando mais rápido.*/

