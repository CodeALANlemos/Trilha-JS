// JavaScript característica:
/* tipagem dinâmica: uma linguagem que é dinâmicamente tipada é quando não precisamos declarar o tipo. Quer dizer que o JavaScript aceita que nós, 
por exemplo, criemos uma let, vou chamar de minhaVar e dê um valor para ela de número, por exemplo 4738, E mais para frente, nós reatribuimos essa variável
por exemplo minhaVar, pode deixar um valor numérico para ter um valor de string. Ou seja, quando criamos uma variável não precisamos dizer para o JavaScript
qual é o tipo de dado, então, não preciso dizer que minhaVar é do tipo number. O JavaScript não aceita isso. Ele vê qual é o tipo e infere essa informação.
*/
